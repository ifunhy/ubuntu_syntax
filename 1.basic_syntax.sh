# 현재 위치 경로를 출력
pwd

# 현재 위치에서 파일 및 폴더 조회
ls

# 목록 조회 자세히
ls -l

# 목록 조회 자세히, 숨김파일까지
ls -al

# 목록 조회 자세히, 숨김파일, 오래된 순으로 정렬(최신은 alt)
ls -alrt

# 디렉토리 생성
mkdir jiki

# 특정 디렉토리로 이동(절대경로는 /home/...)
cd jiki

# 상위 디렉토리로 이동
cd ..
# 또는 cd ../

# root 디렉토리로 이동
cd /

# 직전 폴더로 이동
cd -

# home 경로로 이동
cd
cd ~

# 상대경로와 절대경로
# 상대경로: cd ../../
# 절대경로: cd /home/..